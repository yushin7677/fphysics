    if(Math.abs(object.N*object.mu) > Math.abs(object.F) && Math.abs(object.vx) < 0.1){
	object.ax = 0;
	object.vx = 0;
        object.Ftr = - object.F;
    }
    else{
	object.ax = (object.F - Math.sign(object.vx)*Math.abs(object.N*object.mu))/object.m;
        object.Ftr = - Math.sign(object.vx)*Math.abs(object.N*object.mu)/object.m;
    }
