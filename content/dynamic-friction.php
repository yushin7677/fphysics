Так, хорошо. Если мы толкаем брусок по столу, шкаф по полу, то все понятно. А что происходит, если мы катим тележку? Так вот, когда мы что-то катим, то никакого скольжения нет!!! Колесо соприкасается с землей одной единственной точкой:

<img src="/images/weel.jpg">

Поэтому, сила трения между колесом и землей совершенно не мешает нам эту тележку катить! Совершенно!!! Ну то есть, самую малость, которой можно пренебречь. Именно поэтому колесные транспортные средства очень легко двигать.
Стоп, тогда почему тележка тоже останавливается? Потому что в тележке колесо трется об ось! Либо ось трется о крепление (тут уже зависит от конструкции). Как считать такое трение? Очень просто, говорим, что у тележки есть mu, и с его помощью считаем, так называемую, силу трения качения. Все!)